// Fonction utilitaire pour encoder correctement les messages avec emojis et caract√®res sp√©ciaux
function encodeMessage(text) {
    // S'assurer que le texte est en UTF-8
    if (!text) return '';
    
    // Convertir le texte en string si ce n'est pas d√©j√† le cas
    text = String(text);
    
    // Retourner le texte tel quel - Node.js g√®re nativement l'UTF-8
    // Les emojis et caract√®res sp√©ciaux seront correctement trait√©s
    return text;
}

// Fonction pour √©chapper les caract√®res HTML si n√©cessaire
function escapeHtml(text) {
    if (!text) return '';
    // √âchapper seulement les caract√®res HTML critiques
    return text
        .replace(/&/g, '&amp;')
        .replace(/</g, '&lt;')
        .replace(/>/g, '&gt;');
}

// Fonction pour pr√©parer les options de message avec le bon encodage
function prepareMessageOptions(options = {}, useHtml = false) {
    // Si useHtml est false, ne pas utiliser parse_mode pour √©viter les probl√®mes
    const defaultOptions = {
        disable_web_page_preview: false,
        ...options
    };
    
    // Ajouter parse_mode seulement si explicitement demand√©
    if (useHtml) {
        defaultOptions.parse_mode = 'HTML';
    }
    
    return defaultOptions;
}

